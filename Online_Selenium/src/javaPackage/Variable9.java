package javaPackage;

public class Variable9 {

	public static void main(String[] args) {

		int a = 5; // declaration of local variable
		a = 10; // assigning the value of a variable
		System.out.println(a); // 10- calling a variable

		char b = 'h'; // defining a variable
		System.out.println(b); // h - calling b varible

		String c = "a";
		System.out.println(c);// a

		String str = "Hello Selenium";
		System.out.println(str);// Hello Selenium
	}

}
